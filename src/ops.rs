/// `OperatorRegistry` holds the key information to identify operators and generate optimized
/// computation plans. It has an operator table where each value contains a mapping from a pair of
/// the corresponding operator and symbolic shapes to a symbolic expression for absolute number of
/// floating point operations (FLOPs), which we call symbolic transformation. These expressions are
/// created by measuring the absolute number of FLOPs or multiply-accumulates (MACs) for FMA
/// instructions required for each pair of operator and symbolic shapes, for a given size such as
/// sequence length.
///
/// Note: To register a new operator, please follow the instructions below:
///
/// * First, declare a new operator as an enumerator value of `Operator` in the FlatBuffers schema,
///   i.e., `flatflow/ops/operator.fbs`. The enumerator values are recommended to be sorted in order
///   of their original operator names (not the enumerator values themselves) for searching
///   convenience.
/// * Second, generate codes from the updated schema by running `make generate` in the root
///   directory of this source tree. This will create `src/ops/operator_generated.rs`, etc.
/// * Third, add a specialized implementation of `Fn` for the new operator; please refer to the
///   implementations below for supported operators. This step is checked at compile time.
/// * Finally, register the new operator to the operator table by calling
///   `OperatorRegistry::register` in the constructor below.
///   For the Python frontend, add the ATen counterpart and enumerator value as key and value
///   respectively to the operator table in `flatflow/ops/ops.py`.
#[derive(Clone, Debug, Eq, PartialEq)]
pub struct OperatorRegistry {
    vtable: HashMap<Operator, fn(Vec<TensorMetadata>, TensorMetadata) -> Polynomial>,
}

