// Subset<>
//
// Represents a partition generated by the balanced largest differencing method.
//
// NOTE: `Key` does not necessarily have to be an arithmetic type. Key types
// other than `Arithmetic` may also be supported by adding `Compare` to the
// template arguments, just like associative containers.
template <typename Key, typename Data>
  requires Arithmetic<Key>
class Subset {
 public:
  using key_type = Key;
  using value_type = std::vector<Data>::value_type;

  Subset() {}

  Subset(key_type key, const value_type &item) {
    sum_ = key;
    items_ = {item};
  }

  Subset(key_type key, value_type &&item) {
    sum_ = key;
    items_ = {std::move(item)};
  }

  Subset(key_type key, const std::vector<Data> &items) : items_(items) {
    sum_ = key;
  }

  Subset(key_type key, std::vector<Data> &&items) : items_(std::move(items)) {
    sum_ = key;
  }

  Subset(const Subset &other) = default;

  Subset &operator=(const Subset &other) = default;

  Subset(Subset &&other) = default;

  Subset &operator=(Subset &&other) = default;

  key_type &sum() { return sum_; }

  key_type sum() const { return sum_; }

  std::vector<Data> &items() { return items_; }

  const std::vector<Data> &items() const { return items_; }

  bool operator<(const Subset &other) const noexcept {
    return sum_ < other.sum();
  }

  // Subset::Join()
  //
  // Merges the two given partitions.
  void Join(Subset &other) {
    sum_ += other.sum();
    items_.reserve(items_.size() + other.items().size());
    items_.insert(items_.cend(), std::make_move_iterator(other.items().begin()),
                  std::make_move_iterator(other.items().end()));
  }

 protected:
  key_type sum_;
  std::vector<Data> items_;
};

// Solution<>
//
// Represents a partial solution used in the balanced largest differencing
// method.
template <typename InputIt, typename F, typename Proj>
class Solution {
 public:
  using key_type = std::invoke_result_t<Proj, std::iter_value_t<InputIt>>;
  using value_type = std::invoke_result_t<F, std::iter_value_t<InputIt>>;

  Solution() {}

  Solution(InputIt first, InputIt last, F func, Proj proj) {
    subsets_.reserve(std::distance(first, last));
    std::for_each(std::execution::seq, std::make_move_iterator(first),
                  std::make_move_iterator(last), [&](auto item) {
                    subsets_.emplace_back(proj(item), func(item));
                  });
    difference_ = subsets_.back().sum() - subsets_.front().sum();
  }

  Solution(const Solution &other) = default;

  Solution &operator=(const Solution &other) = default;

  Solution(Solution &&other) = default;

  Solution &operator=(Solution &&other) = default;

  key_type &difference() { return difference_; }

  key_type difference() const { return difference_; }

  std::vector<Subset<key_type, value_type>> &subsets() { return subsets_; }

  const std::vector<Subset<key_type, value_type>> &subsets() const {
    return subsets_;
  }

  bool operator<(const Solution &other) const noexcept {
    return difference_ < other.difference();
  }

  // Solution::Combine()
  //
  // Differences the given two solutions by joining the subset with the smallest
  // sum in one with the subset with the largest sum in the other, the subset
  // with the second smallest sum in one with the subset with the second largest
  // sum in the other, and so on.
  void Combine(Solution &other) {
    // clang-format off
    #pragma omp parallel for
    for (std::size_t index = 0; index < subsets_.size(); ++index) {
      subsets_[index].Join(other.subsets()[subsets_.size() - index - 1]);
    }
    // clang-format on

    std::sort(std::execution::par, subsets_.begin(), subsets_.end());
    difference_ = subsets_.back().sum() - subsets_.front().sum();
  }

 protected:
  key_type difference_;
  std::vector<Subset<key_type, value_type>> subsets_;
};

// BLDM()
//
// Partitions the given items using the balanced largest differencing method
// (BLDM) from the paper `The Differencing Algorithm LDM for Partitioning:
// A Proof of a Conjecture of Karmarkar and Karp`, a variant of LDM for
// balanced number partitioning with larger cardinalities.
// Please refer to https://www.jstor.org/stable/3690207.
//
// NOTE: Description of the algorithm and the proof of performance ratios are
// provided by Michiels, Aarts, Korst, van Leeuwen and Spieksma from the paper
// `Computer-assisted proof of performance ratios for the Differencing Method`.
// See https://doi.org/10.1016/j.disopt.2011.10.001 for the details.
template <typename InputIt, typename OutputIt, typename F, typename Proj>
OutputIt BLDM(InputIt first, InputIt last, OutputIt result, F func, Proj proj,
              typename std::iterator_traits<InputIt>::difference_type m) {
  const auto n = std::distance(first, last);

  if (n == 0) {
    return result;
  }

  CHECK_NE(m, 0);
  CHECK_EQ(n % m, 0);

  // Initially, BLDM starts with a sequence of `k` partial solutions, where each
  // partial solution is obtained from the `m` smallest remaining items.
  auto solutions = std::priority_queue<Solution<InputIt, F, Proj>>();

  for (typename std::iterator_traits<InputIt>::difference_type offset = 0;
       offset < n; offset += m) {
    solutions.emplace(std::next(first, offset), std::next(first, offset + m),
                      func, proj);
  }

  // Next, the algorithm selects two partial solutions from the sequence, for
  // which the difference between the maximum and minimum subset sum is largest.
  // These two solutions are combined into a new partial solution by joining the
  // subset with the smallest sum in one solution with the subset with the
  // largest sum in another solution, the subset with the second smallest sum in
  // one solution with the subset with the second largest sum in another
  // solution, and so on. This process is called differencing the solutions.
  // The combined solution replaces the two solutions in the sequence, and we
  // iterate this differencing operation until only one solution in the sequence
  // remains, which is the balanced solution obtained by BLDM.
  while (1 < solutions.size()) {
    auto solution = solutions.top();
    solutions.pop();

    solution.Combine(
        const_cast<typename std::priority_queue<
            Solution<InputIt, F, Proj>>::reference>(solutions.top()));
    solutions.pop();

    solutions.emplace(std::move(solution));
  }

  return std::move(std::execution::par, solutions.top().subsets().begin(),
                   solutions.top().subsets().end(), result);
}

// partition()
//
// Partitions the given items in the range [`first`, `last`) into `m` subsets
// where each subset contains items with `func` applied, which are evaluated as
// their projected values through `proj`. The resulting subsets are stored in an
// output range starting from `result`.
template <typename InputIt, typename OutputIt, typename F, typename Proj>
OutputIt partition(InputIt first, InputIt last, OutputIt result, F func,
                   Proj proj,
                   typename std::iterator_traits<InputIt>::difference_type m) {
  return BLDM(first, last, result, func, proj, m);
}

